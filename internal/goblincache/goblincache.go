// Package goblincache is for caching requests from OSRS wiki prices api
package goblincache

import (
	"sync"
	"time"
)

type cacheEntry struct {
	createdAt time.Time
	val       []byte
}

type Cache struct {
	cacheMap map[string]cacheEntry
	mu       *sync.Mutex
}

func New(interval time.Duration) (*Cache, error) {
	newCache := &Cache{
		cacheMap: make(map[string]cacheEntry),
		mu:       &sync.Mutex{},
	}
	return newCache, nil
}
